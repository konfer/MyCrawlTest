package com.konfer.entity;

public class Page
{
    private String content;
    private String allNum;//总播放量
    private String dayNum;
    private String commentNum;
    private String collectNum;
    private String supportNum;
    private String againstNum;

    private String tvNum;
    private String url;
    private String episodeNum;

    public String getContent()
    {
        return content;
    }

    public void setContent(String content)
    {
        this.content = content;
    }

    public String getAllNum()
    {
        return allNum;
    }

    public void setAllNum(String allNum)
    {
        this.allNum = allNum;
    }

    public String getDayNum()
    {
        return dayNum;
    }

    public void setDayNum(String dayNum)
    {
        this.dayNum = dayNum;
    }

    public String getCommentNum()
    {
        return commentNum;
    }

    public void setCommentNum(String commentNum)
    {
        this.commentNum = commentNum;
    }

    public String getCollectNum()
    {
        return collectNum;
    }

    public void setCollectNum(String collectNum)
    {
        this.collectNum = collectNum;
    }

    public String getSupportNum()
    {
        return supportNum;
    }

    public void setSupportNum(String supportNum)
    {
        this.supportNum = supportNum;
    }

    public String getAgainstNum()
    {
        return againstNum;
    }

    public void setAgainstNum(String againstNum)
    {
        this.againstNum = againstNum;
    }

    public String getTvNum()
    {
        return tvNum;
    }

    public void setTvNum(String tvNum)
    {
        this.tvNum = tvNum;
    }

    public String getUrl()
    {
        return url;
    }

    public void setUrl(String url)
    {
        this.url = url;
    }

    public String getEpisodeNum()
    {
        return episodeNum;
    }

    public void setEpisodeNum(String episodeNum)
    {
        this.episodeNum = episodeNum;
    }
}
